        <h2>Novel</h2>
        <p>Novel adalah suatu karya sastra berbentuk prosa naratif yang panjang, dimana di dalamnya terdapat rangkaian cerita tentang kehidupan seorang tokoh dan orang-orang di sekitarnya dengan menonjolkan sifat dan watak dari setiap tokoh dalam novel tersebut.</p>
        <br>
        <p>Ada juga yang mengatakan bahwa pengertian novel adalah suatu karangan berbentuk prosa yang di dalamnya terdapat unsur intrinsik dan unsur ekstrinsik. Tidak seperti cerpen (cerita pendek), isi cerita sebuah novel jauh lebih panjang dan kompleks, serta terdapat pesan tersembunyi yang ingin disampaikan kepada pembacanya.</p>
        <br>
        <p>Secara etimologis, kata “novel” diserap dari bahasa Italy, yaitu “novella” yang artinya sebuah cerita. Sedangkan orang yang menulis cerita novel disebut sebagai novelis.</p>
